def line_break
	puts "*"*50
	puts "*"*50
end 

def question_prompt(question, options)
	puts question, options 
	gets.chomp.downcase 
end 

def say(narrator, message)
	if narrator == nil 
		puts message
	else 
	puts "Narrator: #{message}"
	end 
end 

def display_gender(gender)
	case gender 
	when "girl"
		say nil, 
		"Your name is Furiosa Krishna. 
		With a cocksure attitude and a repetoire of quoutable action-movie one-liners, 
		you left the safety of your nearby village in search of supplies for your friends and family. 
		You'll sacrifice anything to survive in this hellish reality."
	when "boy"
		say nil, 
		"		Your name is Max the Meek. It rhymes with weak. 
		Timid and weak as you are, you nonetheless possess lightning-fast feet, allowing you to run away from just about any situation. 
		You're not exactly hero material, but you'll do."
	when "neither"
		say nil, 
		"You refuse to conform to such close-minded, binary classifications such as 'Male' or 'Female'. 
		Such labels are irrelevant in this post-apocalyptic hellhole. 
		There are two types of people left in this world: the strong and the weak. And you're most definitely the former." 
	else 
		say nil, "You refuse to play along, eh?"
		say nil, "Well then, guess what? You died in the nuclear fire that consumed 99% of all life on the planet."
		say nil, "Game Over."
	end 
end 

def choose_weapon(weapon)
	case weapon 
	when "rifle"
		say nil, "You ready your trusty high-caliber, long-range .308 hunting rifle -- a keepsake from your late father. The perfect choice for delivering a lethal force from a distance."
	when "machete"
		say nil, "You ready your trusty machete, the perfect weapon for dispatching a threat efficiently and quietly."
	when "bat"
		say nil, "You ready your trusty Lil' Sluggers Baseball Bat. It isn't the quietest weapon, but it always gets the job done."		
	else 	
		say nil, "Sorry, but you left that back at home. In that split second of indecisiveness, a mutant ghouls attacks you. Your trusty companion attempts to warn you, but it's two late: the ghoul has already bitten into your neck. Your vision dims." 
		say nil, "Game Over."
	end
end 

def battle_options(choice)
	case choice 
	when "fight"
		say nil, "You take aim and deliver a critical blow. Zombie Ed fainted."
	when "code"
		say nil, "You put away your weapon, pull out your laptop, and finishing coding the 'Cat in the Hat' homework problem! Zombie Ed is impressed! He no longer desires your brains!"
	when "use item"
		say nil, "You check your backpack. Your inventory is empty! Zombie Ed lunges. You scream. Your vision blurs..."
	when "run away"
		say nil, "You attempt to run away, but you trip, and suddely Zombie Ed is upon you..."	
	end 
end 

